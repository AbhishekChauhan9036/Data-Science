# Lists in Python
fruits = ["apple", "banana", "cherry"]
fruits.append("orange")
print(fruits)  # Output: ['apple', 'banana', 'cherry', 'orange']

"""
Common Operations:
append(), insert(), remove(), pop(), sort(), reverse(), len()
Indexing and slicing: fruits[0], fruits[-1], fruits[1:3]
"""


# Tuples in Python
coordinates = (10.5, 20.7)
print(coordinates[0])  # Output: 10.5

"""
Tuple Operations:
Indexing and slicing: my_tuple[0], my_tuple[1:]
Functions: len(), count(), index()
Tuples can be used as keys in dictionaries
"""



