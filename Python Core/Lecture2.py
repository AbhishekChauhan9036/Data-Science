# If-Else
age = 18
if age >= 18:
    print("You can vote.")
else:
    print("You are too young to vote.")


# Ternery Operator
status = "Adult" if age >= 18 else "Minor"
print(status)
# Output: Adult



# Nested If-Else
marks = 85
if marks >= 60:
    if marks >= 80:
        print("Grade: A")
    else:
        print("Grade: B")
else:
    print("Grade: C")



# While loop
i = 1
while i <= 5:
    print(i)
    i += 1


# For Loop
for i in range(5):
    print(i)
